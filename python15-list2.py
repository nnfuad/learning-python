marks = [95, 98, 97, "maths"]
print(marks)  # Prints the entire list
i=0
while i<len(marks):
    print(marks[i])
    i+=1

# Using a basic for loop to print each element in the list
for mark in marks:
    print(mark)

# Using range and indexing to print each element in the list
for i in range(len(marks)):
    print(marks[i])
    
